
public class VarDataTypes {
	
	public static void main(String[] args) {
		
		// for more than one character
		String name = "Sofía Díaz";
		int age = 20;
		
		// for only one character
		char initial = 'S';
		
		// true or false
		boolean married = false;
		
		// types of integer data  
		
		// byte
		byte add = 23;
		
		// short 
		short mult = 20 + 300;
		
		// int
		int profit = 35546445; 
		
		//long
		long citizens = 100_000_000;
		System.out.println(citizens);
		
		// float
		float mean = 8.3f;
		
		// double 
		double pi = 3.1415934324234;
		
		
	}
	
}
